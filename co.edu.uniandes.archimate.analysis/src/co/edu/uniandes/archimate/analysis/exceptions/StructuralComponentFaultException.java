package co.edu.uniandes.archimate.analysis.exceptions;

public class StructuralComponentFaultException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = 4459564993599097802L;

	public StructuralComponentFaultException() {
	}

	public StructuralComponentFaultException(String arg0) {
		super(arg0);
	}

	public StructuralComponentFaultException(Throwable arg0) {
		super(arg0);
	}

	public StructuralComponentFaultException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

}
